class Solution:
    def isValid(self, s: str) -> bool:
        stack = []
        # Создаем словарь для быстрой проверки пар: ключ - закрывающая, значение - открывающая
        close_to_open = {")": "(", "]": "[", "}": "{"}
        
        # Перебираем символы прямо по ходу строки (нам даже индексы i не нужны!)
        for char in s:
            # Если это закрывающая скобка
            if char in close_to_open:
                # Проверяем, что стек не пуст и верхний элемент совпадает с нужной открывающей
                if stack and stack[-1] == close_to_open[char]:
                    stack.pop()  # Нашли пару — удаляем из стека
                else:
                    return False  # Ошибка: либо стек пуст, либо скобки не совпали
            else:
                # Если это открывающая скобка — просто кладем в стек
                stack.append(char)
                
        # В конце стек должен быть пустым (все скобки нашли свои пары)
        return len(stack) == 0     